// Variables - They are container whose storing the data.

#include<iostream>
using namespace std;
int main(){
    int a = 10; // Integer variable
    float b = 3.14; // Float variable
    char c = 'A'; // Character variable
    string d = "Hello"; // String variable

    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Character: " << c << endl;
    cout << "String: " << d << endl;

    return 0;
}

// Comments - They are used to explain the code and make it more readable. They are ignored by the compiler.
